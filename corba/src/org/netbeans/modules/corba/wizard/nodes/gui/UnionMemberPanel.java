    private void checkState () {
        if (IdlUtilities.isValidIDLIdentifier(this.name.getText()) && 
	    this.type.getText().length() >0 && 
	    this.label.getText().length() >0 &&
            IdlUtilities.validLength(this.length.getText())) {
            enableOk();
        }
        else {
            disableOk();
        }
    }
