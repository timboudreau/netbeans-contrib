    public Class getTargetClass() {
        String s = jTextField1.getText();
        s = s.trim();
        if (s.length() == 0) {
            return null;
        }
        try {
            Class result = Class.forName(s);
            return result;
        } catch (Exception e) {
            return null;
        }
    }
    
    
    
